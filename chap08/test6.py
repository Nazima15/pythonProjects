import pandas as pd

data = {
    "2015": [9904312, 3448737, 2890451, 2466052],
    "2010": [9631482, 3393191, 2632035, 2500000],
    "2005": [9762546, 3512547, 2586846, 2524281],
    "2000": [9853972, 3512547, 2526846, 2564281],
    "지역": ["수도권", "경상권", "수도권", "경상권"],
    "2010-2015 증가율": [0.0283, 0.0163, 0.0982, 0.0141]
}

index = ['서울', '부산', '인천', '대구']
columns = ["지역", "2015", "2010", "2005", "2000", "2010-2015 증가율"]

df = pd.DataFrame(data, index=index, columns=columns)

# 1. 위치 기준 슬라이싱 (1~2번째 행)
print(df.iloc[1:3])
print('-----------------------------------------------------------------')

# 2. 라벨 기준 슬라이싱 (서울~부산)
print(df.loc["서울":"부산"])
print('-----------------------------------------------------------------')

# 3. 라벨 기반, 위치 기반 모두 안전하게 접근
print(df.loc["서울", "2015"], df["2015"].iloc[0])
